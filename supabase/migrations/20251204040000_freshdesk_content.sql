## ⚙️ 티켓 자동화 마스터하기

### Dispatch'r (자동 배정)

티켓 생성 시 자동으로 분류 및 배정합니다.

```
IF 제목에 "결제" 포함
THEN 그룹 = "결제팀", 우선순위 = High
```

### Supervisor (시간 기반)

특정 시간 경과 후 자동 액션을 수행합니다.

```
IF 상태 = Pending AND 48시간 경과
THEN 알림 전송 + 상태 = Open
```

### Observer (이벤트 기반)

티켓 이벤트 발생 시 즉시 반응합니다.

```
IF 상태가 Resolved로 변경됨
THEN CSAT 설문 전송
```

