### Freshdesk SLA 설정

| 지표 | 설명 |
|------|------|
| **First Response Time** | 첫 응답까지 시간 |
| **Resolution Time** | 완전 해결까지 시간 |
| **Every Response Time** | 매 응답 간격 |

### 우선순위별 SLA 예시

| 우선순위 | 첫 응답 | 해결 |
