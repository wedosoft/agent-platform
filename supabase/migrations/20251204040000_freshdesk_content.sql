## 🎫 Freshdesk 티켓의 이해

**티켓**은 고객의 문의, 요청, 불만을 체계적으로 추적하고 해결하기 위한 기록 단위입니다.

### 티켓의 장점

| 이메일만 사용 | Freshdesk 티켓 사용 |
|--------------|-------------------|
| 문의가 받은편지함에 묻힘 | 모든 문의를 한 곳에서 관리 |
| 누가 처리 중인지 모름 | 담당자가 명확히 표시됨 |
| 히스토리 추적 어려움 | 전체 대화 이력 보존 |
| 성과 측정 불가 | 응답 시간, 해결률 추적 |

